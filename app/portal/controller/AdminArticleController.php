                $this->error($result);
            }

            $portalPostModel = new PortalPostModel();
            $portalPostModel->adminEditArticle($data['post'], $data['post']['categories'], $data['post']['schools']);

            $this->success('保存成功!', url('AdminArticle/index'));

        }
    }

